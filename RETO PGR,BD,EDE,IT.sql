CREATE FUNCTION callSellAndSubstract(NAME_I VARCHAR(50), AMOUNT INT, PROD BOOLEAN) RETURNS INT
READS SQL DATA
BEGIN
    DECLARE CURRENTSTOCK INT DEFAULT 0;
    DECLARE STOCKCHECK INT DEFAULT 0;
    DECLARE COD INT;
    DECLARE ERROR BOOLEAN DEFAULT FALSE;
    
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET ERROR = TRUE;
    
    IF PROD THEN
        SET COD:=(SELECT CODPRODUCT FROM PRODUCT WHERE NAMEP = NAME_I LIMIT 1);
        IF COD IS NULL THEN
            SET ERROR = TRUE;
        END IF;

        IF ERROR = FALSE THEN
            SET STOCKCHECK:=(SELECT STOCKPRODUCT FROM PRODUCT WHERE CODPRODUCT = COD);
            SET CURRENTSTOCK:=STOCKCHECK - AMOUNT;
            IF CURRENTSTOCK < 0 THEN
                SET ERROR = TRUE;
            END IF;
        END IF;
    ELSE
        SET COD:=(SELECT CODCOMPONENT FROM COMPONENT WHERE NAMECOMP = NAME_I LIMIT 1);
        IF COD IS NULL THEN
            SET ERROR = TRUE;
        END IF;

        IF ERROR = FALSE THEN
            SET STOCKCHECK:=(SELECT STOCKCOMPONENT FROM COMPONENT WHERE CODCOMPONENT = COD);
            SET CURRENTSTOCK:=STOCKCHECK - AMOUNT;
            
            IF CURRENTSTOCK < 0 THEN
                SET ERROR = TRUE;
            END IF;
        END IF;
    END IF;
    
    IF ERROR = TRUE THEN
		SET CURRENTSTOCK=STOCKCHECK;
    END IF;
    RETURN CURRENTSTOCK;
END //
