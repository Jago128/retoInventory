		DECLARE TOTALPRICE DOUBLE;
		
		SET STOCK:=(SELECT callSellAndSubstract(NAME_I, AMOUNT, PROD));
		IF PROD THEN
			SET COD:=(SELECT CODPRODUCT FROM PRODUCT WHERE NAMEP = NAME_I LIMIT 1);
